def edit_dist(d1, d2, pen):
    n1 = len(d1) + 1
    n2 = len(d2) + 1
    matrix = np.zeros((n1, n2), dtype=int)
    for i in range(0, n1):
        matrix[i, 0] = i * -pen
    for j in range(1, n2):
        matrix[0, j] = j * -pen
    for i in range(1, n1):
        for j in range(1, n2):
            scores = [matrix[i-1, j] - pen, matrix[i, j-1] - pen, matrix[i-1, j-1] +
                      score_matix[d1[i-1]][d2[j-1]]]
            matrix[i, j] = max(scores)
    return matrix

